"""
字符串连接  使用+号
"""

# 将姓和名 存储在不同的变量中，显示姓名时合二为一
first_name = "san"  # 名
last_name = "zhang"   # 姓
# 名 + 姓 得到完整的名字 并输出
full_name = first_name + " " + last_name
print(full_name)


